                <p class="method-label">Kindergarten, SRP, HATC</p>
                <p class="method-description">Another school of thought or method of teaching adopted at Toys n Joy
                    Playschool
                    is the Reggio Emilia Method. Evolved in Italy, inherently, this approach believes in
                    teaching through activities. What makes this approach different and vital to Toys n
                    Joy Playschool’s teaching methodology is how we inculcate different types of
                    activities and the core values it promotes. The approach believes in & is aimed at
                    enabling the child to make their own place in society. It encourages communication
